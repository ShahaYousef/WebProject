        <p>The Areca palm, native to Madagascar, is one of the most popular indoor houseplants sold
       today. Indoors an Areca palm is a medium sized exotic looking plant that can reach a height
      of 6-8 feet; outdoors it may be as tall as 25 feet. The Areca palm gets its nickname, the
      Butterfly palm, because its long feathery fronds (leaves) arch upwards off multiple reed- like
      stems, resembling butterfly wings. </p>
        <p> Each frond has between 40-60 leaflets on it. When first
       bought, Areca palms are a delight, inexpensive good-sized plants with beautiful green
       upright fronds. However, over time, the overall appearance of an Areca palm may diminish;
       the older bottom fronds turn yellow and the larger fronds droop and bend.
       </p>

