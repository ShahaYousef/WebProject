          <h4> <i style="color: #FFBD2E; font-size: 25px" class="fas fa-sun"></i> Light </h4>
          <p > Areca palms require bright indirect light. Too much light or direct sun
             burns the fronds and causes them to yellow.</p>
          <pre>
          </pre>

          <!-- Part2 -->
          <h4> <i style="color: #1167b1; font-size: 25px" class="fas fa-tint"></i> Water </h4>
          <p> Keep the soil of an Areca palm moist but never soggy. Allow the top couple of inches to dry out before watering. Remember, the soil at the bottom of the container is much wetter than the soil at the top. Never allow an Areca palm to sit in water. If your household water has a lot of
             chemicals in it or passes through a water softener, allow it to sit out over- night before using it or use distilled water.</p>
          <pre>
          </pre>

          <!-- Part3 -->
          <h4> <i style="color: #6B8E23; font-size: 25px" class="fas fa-spray-can"></i> Humidity </h4>
          <p> High humidity is essential for the fronds of an Areca palm to keep looking good.</p>
